/******************************************************************************

Relational Operator: <, <= , >, >=, ==, !=

*******************************************************************************/
public class Main
{
	public static void main(String[] args) {
	    int a=10,b=2;
		System.out.println("A<B  : "+(a<b));
		System.out.println("A<=B : "+(a<=b));
		System.out.println("A>B  : "+(a>b));
		System.out.println("A>=B : "+(a>=b));
		System.out.println("A==B : "+(a==b));
		System.out.println("A!=B : "+(a!=b));
	}
}
