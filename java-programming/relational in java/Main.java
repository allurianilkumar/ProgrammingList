/******************************************************************************
Relational Operators:

    <       1<2 => true
    <=      1<=1  => true
    >       1>1  => false
    >=      1>=1 => true
    ==      1==1 => true
    !=      1!=1  => false

*******************************************************************************/
public class Main
{
	public static void main(String[] args) {
	    int a=10,b=20;
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
}

