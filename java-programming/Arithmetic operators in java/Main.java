// Arithmetic Operators : +,-,*,/,%
public class Main
{
	public static void main(String[] args) {
	    int a=10,b=2;
		System.out.println("A+B="+(a+b)); // 12
		System.out.println("A-B="+(a-b)); // 8
		System.out.println("A*B="+(a*b)); // 20
		System.out.println("A/B="+(a/b)); // 5
		System.out.println("A%B="+(a%b)); // 0
	}
}

