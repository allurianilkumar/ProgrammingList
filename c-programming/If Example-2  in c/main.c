/*

if-else condition:
------------------

Syntax:
-------
if(condition){
    true part
}else{
    false part
}

*/
#include<stdio.h>
void main(){
    int a=1,b=10;
    if(a>b){
        printf("True\n");
    }else{
        printf("False: A>B\n");
    }
    if(a+9 == b)
        printf("TRUE\n");
    else
        printf("FALSE: A+10 == B\n");
    printf("Done");
}




