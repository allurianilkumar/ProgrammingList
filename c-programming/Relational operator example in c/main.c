// Relational operator in c
// symbols: < , <= , > ,>= , == , !=
// It is used between the two values or variables 
// for comparision.
#include<stdio.h>
// true=1, false=0
void main(){
    // Initialization of the a=1 and b=2
    int a=1,b=2;
    // <
    printf("A<B  = %d\n",a<b); // 1
    // <=
    printf("A<=B = %d\n",a<=b); // 1
    // >
    printf("A>B  = %d\n",a>b); // 0
    // >=
    printf("A>=B = %d\n",a>=b); // 0
    // ==
    printf("A==B = %d\n",a==b); // 0
    // !=
    printf("A!=B = %d\n",a!=b); // 1
}





