#include<stdio.h>
void main(){
    printf("MOM"); // MOM
    printf("\n"); // Taking a new line
    printf("\"MOM\""); // "MOM"
    printf("\n");
    printf("\'MOM\'"); // 'MOM'
    printf("\n");
    printf("\\MOM\\"); // 
    printf("\n");
    printf("A=%d;\n",100); // 100 is value
    printf("A=%c;\n",'a');
    printf("%f\n",100.5); // 100.500000
    printf("%.3f\n",100.5); // 100.500
    printf("%.0f\n",100.5); // 100
    printf("%.0f\n",101.5); // 102
    printf("%.0f\n",106.5); // 106
    printf("%s\n","MOM IT Solutions"); // MOM IT Solutions
    printf("%d\n",(1<2)); // 1
    printf("%d\n",(1>2)); // 0
    
}


