# +, -, *, **, /, //, % are Arithmetic Operators
print(1+1) # 2 -> int+int =>int 
print(1+1.5) # 2.5 -> int+float=float
print(1-1.5) # 2.5 -> int-float=float
print(2*1.5) # 3.0 -> int*float=float
print(10/2) # 5.0 -> int/int=float
print(10//2) # 5  -> int//int=int
print(10.0//2) # 5.0 -> float//int=float
print(10//2.0) # 5.0 -> int//float=float
print("*") # *
print(10*10) # 100
print("*"*5)
print("MOM "*5)
print(10**3) # 10 * 10 * 10 = 1000
print(11%2) # 1 -> int%int=int
print(11%2.0) # 1.0 -> int%float=float
print(11.0%2.0) # 1.0 -> float%float=float
print(11.0%2) # 1.0 -> float%int=float

