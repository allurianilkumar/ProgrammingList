'''
Convertions

'''
# str to int
sid="123"
print ('Str type of sid=',sid)
print ('Str type of sid=%s'%sid)
sid=int(sid)
print ('int type of sid=%d'%sid)


#int to float

sid=float(sid)
print ('float type of sid=%f'%sid)
print ('float type of sid=%.2f'%sid)
print ('float type of sid=%.0f'%sid)

sid=sid+0.50
print ('Update wiht 0.50 ==> sid=%.2f'%sid)
#float to int
sid=int(sid)
print("float to int sid=%d"%sid)

# int to str

sid=str(sid)
print("int to str sid=%s"%sid)

